
public class Reserva {
	public String email;
	public String numberDays;
	public String numberPeoples;
	public String date;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getNumberDays() {
		return numberDays;
	}
	public void setNumberDays(String numberDays) {
		this.numberDays = numberDays;
	}
	public String getNumberPeoples() {
		return numberPeoples;
	}
	public void setNumberPeoples(String numberPeoples) {
		this.numberPeoples = numberPeoples;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
}
